package com.aquatp.calculator.calculus;

public class Calculator {

    public double sum(double val1, double val2) {
        return val1 + val2;
    }

    public double subtract(double val1, double val2) {
        return val1 - val2;
    }

    public double divide(double val1, double val2) { return val1 / val2; }

    public double multiply(double val1, double val2) {
        return val1 * val2;
    }

    public double squareRoot(double val) { return Math.sqrt(val); }

}
